#!/usr/bin/env python3
"""Stage 7B model-builder benchmark runner.

Runs the unified benchmark registry over the registered 1D model presets
(Heisenberg, spinless fermion t-V) on CPU and (opt-in) a V100/TITAN V GPU,
and writes ``docs/MODEL_BUILDER_REPORT.md`` + a JSON results file. The
registry records the Stage-7A+ timing contract (model, N, chi, solver, device,
device_name, dtype, runtime, speedup, final_energy, exact_error,
below_ground, gpu_skip_reason).

GPU rules (Stage 7A unified selector — scripts/gpu_selector.py):
- GPU is OPT-IN: runs only when LATTICETN_RUN_GPU=1.
- The unified selector picks a GPU whose name contains ``V100`` or
  ``TITAN V``/``Titan V``. If none matches, the GPU portion clean-skips.
- Default runs are CPU-only and never require a GPU.

This is a model/MPO construction layer, NOT a new solver. The AD mainline is
unchanged; exact/DMRG/Lanczos remain reference baselines.
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

import torch as tc

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from latticetn.model_builder import (  # noqa: E402
    heisenberg_model, spinless_fermion_tv_model,
)
from latticetn.benchmarking import benchmark_model  # noqa: E402

DTYPE = tc.complex128


def run_all(steps: int = 120) -> dict:
    cases = [
        ("heisenberg", 4, 4, 1.0, 0.0, 0.0),
        ("heisenberg", 6, 8, 1.0, 0.0, 0.0),
        ("spinless_fermion_tv", 4, 4, 1.0, 0.5, 0.0),
        ("spinless_fermion_tv", 6, 8, 1.0, 0.5, 0.0),
    ]
    results = []
    for name, N, chi, p1, p2, p3 in cases:
        if name == "heisenberg":
            spec = heisenberg_model(N, J=p1)
        else:
            spec = spinless_fermion_tv_model(N, t=p1, V=p2, mu=p3)
        r = benchmark_model(spec, chi=chi, seed=0, steps=steps)
        r["params"] = {"J/t": p1, "V": p2, "mu": p3}
        results.append(r)
    return {
        "convention": (
            "Stage 7B unified 1D model builder + benchmark registry. "
            "Open boundary, d=2, complex128. Heisenberg uses S=sigma/2; "
            "spinless fermion t-V uses Jordan-Wigner (parity string F=(-1)^n). "
            "AD mainline unchanged; this is a model/MPO construction layer."),
        "dtype": str(DTYPE),
        "mainline_statement": (
            "This benchmark evaluates the AD mainline solver (global AD-MPS) "
            "across the registered 1D model presets via the unified model "
            "builder + benchmark registry, on CPU and (opt-in) a V100/TITAN V "
            "GPU. Exact diagonalization is a reference baseline ONLY. The AD "
            "loss path is unchanged; Stage 7B adds only the model/MPO builder "
            "and benchmark registry layers."),
        "results": results,
        "gpu_ran": any(r["gpu_ran"] for r in results),
        "gpu_skip_reason": results[0]["gpu_skip_reason"] if results else None,
        "device_info": results[0]["device_info"] if results else {},
    }


def _e(x, p=6):
    if isinstance(x, float):
        return "inf" if x == float("inf") else f"{x:.{p}e}"
    return str(x)


def render_report_md(report: dict) -> str:
    L = []
    L.append("# Stage 7B Model Builder + Benchmark Registry Report")
    L.append("")
    L.append("Generated by `scripts/run_model_builder_benchmark.py`.")
    L.append("")
    L.append("## Mainline statement")
    L.append("")
    L.append(f"> _{report['mainline_statement']}_")
    L.append("")
    L.append("Stage 7B is a **model/MPO construction layer, NOT a new solver**. "
             "The AD solver is still the differentiable Rayleigh quotient + "
             "autograd + torch optimizer. SVD/QR/canonicalization remain "
             "auxiliary stabilization. Exact/DMRG/Lanczos remain reference "
             "baselines. GPU timing from Stage 7 uses the V100/TITAN V "
             "unified selector.")
    L.append("")
    di = report["device_info"]
    L.append("## Device info")
    L.append("")
    L.append(f"- PyTorch version: `{di.get('torch_version')}`")
    L.append(f"- CUDA available: `{di.get('cuda_available')}`")
    L.append(f"- GPU device count: `{di.get('device_count')}`")
    L.append(f"- GPU name: `{di.get('gpu_name')}`")
    L.append(f"- Device used: `{di.get('device')}`")
    L.append(f"- dtype: `{report['dtype']}`")
    L.append(f"- `LATTICETN_RUN_GPU=1`: `{di.get('env_run_gpu')}`")
    L.append(f"- Allowed GPU filters: `{di.get('allowed_filters')}`")
    L.append("")
    if report.get("gpu_skip_reason"):
        L.append(f"**GPU SKIPPED:** {report['gpu_skip_reason']}")
        L.append("")
    else:
        L.append(f"**GPU RAN** on `{di.get('device')}` "
                 f"(`{di.get('gpu_name')}`).")
        L.append("")

    L.append("## CPU/GPU comparison per model")
    L.append("")
    L.append("| model | N | chi | device | device_name | final E | exact error | "
             "runtime_s | speedup | below ground |")
    L.append("|---|---:|---:|:---:|:---:|---:|---:|---:|---:|:---:|")
    for r in report["results"]:
        cpu = r["cpu"]
        L.append(f"| {r['model']} | {r['N']} | {r['chi']} | cpu | cpu | "
                 f"{cpu['final_energy']:.10f} | {cpu['exact_error']:.2e} | "
                 f"{cpu['runtime']:.3f} | — | {cpu['below_ground']} |")
        if r["gpu"] is not None:
            g = r["gpu"]
            L.append(f"| {r['model']} | {r['N']} | {r['chi']} | {g['device']} | "
                     f"{g['device_name']} | {g['final_energy']:.10f} | "
                     f"{g['exact_error']:.2e} | {g['runtime']:.3f} | "
                     f"{_e(r['speedup'], 3)} | {g['below_ground']} |")
        else:
            L.append(f"| {r['model']} | {r['N']} | {r['chi']} | — | — | — | — | "
                     f"— | — | (GPU skipped) |")
    L.append("")
    L.append("Speedup = CPU runtime / GPU runtime. **The GPU is NOT required "
             "to be faster**: small systems are overhead-dominated.")
    L.append("")

    L.append("## Model builder coverage")
    L.append("")
    L.append("| preset | statistics | dense ref | MPO dense | native energy |")
    L.append("|---|:---:|:---:|:---:|:---:|")
    L.append("| `heisenberg_model` | boson (S=sigma/2) | `operators.heisenberg_dense` | "
             "`MPO.generate_heisenberg` | aligns |")
    L.append("| `spinless_fermion_tv_model` | fermion (JW) | "
             "`operators.spinless_fermion_dense` | "
             "`MPO.generate_spinless_fermion` | aligns |")
    L.append("")
    L.append("Both presets' dense/MPO/native energies align to the existing "
             "references (Stage 1/7A). The fermion preset keeps the JW parity "
             "string; it does NOT degrade to hard-core boson.")
    L.append("")

    L.append("## Overall pass/fail")
    L.append("")
    all_ok = all((not r["cpu"]["below_ground"]) for r in report["results"])
    if any(r["gpu"] is not None for r in report["results"]):
        all_ok = all_ok and all(
            (not r["gpu"]["below_ground"]) and
            (abs(r["cpu"]["final_energy"] - r["gpu"]["final_energy"]) < 1e-5)
            for r in report["results"] if r["gpu"] is not None)
    L.append(f"**pass: `{all_ok}`**")
    L.append("")
    L.append("## Known limitations")
    L.append("")
    L.append("- Stage 7B ships two presets (Heisenberg, spinless fermion t-V). "
             "Hubbard / XXZ / TFI / long-range are future work.")
    L.append("- Model/MPO construction layer only; no new solver. The AD "
             "mainline is unchanged.")
    L.append("- No TDVP, no finite-temperature, no Hubbard, no graded fermionic "
             "tensors.")
    L.append("- Small systems only (N=4/6, chi=4/8); large-N scaling is out of "
             "scope.")
    L.append("- GPU is opt-in (LATTICETN_RUN_GPU=1) and selects only "
             "V100/TITAN V; clean-skips otherwise.")
    L.append("")
    return "\n".join(L)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--fast", action="store_true",
                        help="Run the small --fast benchmark preset (default).")
    parser.add_argument("--list", action="store_true",
                        help="List the registered presets and exit.")
    parser.add_argument("--markdown-output", type=Path,
                        default=ROOT / "docs" / "MODEL_BUILDER_REPORT.md")
    parser.add_argument("--json-output", type=Path, default=None)
    parser.add_argument("--print", action="store_true")
    args = parser.parse_args()

    if args.list:
        print("Registered 1D model presets (Stage 7B):")
        print("  - heisenberg_model(N, J)")
        print("  - spinless_fermion_tv_model(N, t, V, mu)")
        print("Modules:")
        print("  - latticetn/model_builder.py")
        print("  - latticetn/benchmarking.py")
        print("Required scripts:")
        print("  - scripts/model_builder_score.py")
        print("  - scripts/run_model_builder_benchmark.py")
        print("Required docs:")
        for d in ("docs/MODEL_BUILDER_SPEC.md", "docs/MODEL_BUILDER_PROTOCOL.md",
                  "docs/MODEL_BUILDER_REPORT.md",
                  "docs/CLAUDE_PROGRESS_MODEL_BUILDER.md"):
            print(f"  - {d}")
        return 0

    if not args.fast and not args.list:
        args.fast = True

    report = run_all(steps=120)
    all_ok = all((not r["cpu"]["below_ground"]) for r in report["results"])
    if any(r["gpu"] is not None for r in report["results"]):
        all_ok = all_ok and all(
            (not r["gpu"]["below_ground"]) and
            (abs(r["cpu"]["final_energy"] - r["gpu"]["final_energy"]) < 1e-5)
            for r in report["results"] if r["gpu"] is not None)
    report["pass"] = all_ok

    args.markdown_output.parent.mkdir(parents=True, exist_ok=True)
    args.markdown_output.write_text(render_report_md(report), encoding="utf-8")
    if args.json_output:
        args.json_output.parent.mkdir(parents=True, exist_ok=True)
        args.json_output.write_text(json.dumps(report, indent=2, default=str),
                                    encoding="utf-8")
    if args.print:
        print(json.dumps(report, indent=2, default=str))
    print(f"model builder benchmark: pass={report['pass']} "
          f"gpu_ran={report['gpu_ran']} "
          f"report -> {args.markdown_output}")
    return 0 if report["pass"] else 1


if __name__ == "__main__":
    raise SystemExit(main())
