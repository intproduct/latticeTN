#!/usr/bin/env python3
"""Run small Heisenberg MPS benchmark sweeps.

This script is a Stage 2 benchmark runner. It intentionally reuses the Stage 1
`scripts/run_heisenberg_small.py::solve` routine so benchmark development does
not fork the optimizer path.
"""

from __future__ import annotations

import argparse
import json
import math
import os
import sys
import time
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))
sys.path.insert(0, str(ROOT / "scripts"))

from run_heisenberg_small import solve  # noqa: E402

BETHE_E0 = 0.25 - math.log(2.0)

PRESETS = {
    "tiny": [
        {"N": 4, "chi": 4, "steps": 80, "lr": 1e-2, "seed": 0},
    ],
    "fast": [
        {"N": 4, "chi": 4, "steps": 120, "lr": 1e-2, "seed": 0},
        {"N": 4, "chi": 8, "steps": 120, "lr": 1e-2, "seed": 0},
        {"N": 6, "chi": 8, "steps": 200, "lr": 1e-2, "seed": 0},
    ],
    "full": [
        {"N": 4, "chi": 4, "steps": 200, "lr": 1e-2, "seed": 0},
        {"N": 4, "chi": 8, "steps": 200, "lr": 1e-2, "seed": 0},
        {"N": 6, "chi": 8, "steps": 300, "lr": 1e-2, "seed": 0},
        {"N": 6, "chi": 16, "steps": 300, "lr": 1e-2, "seed": 0},
    ],
}


def _env_setup() -> None:
    os.environ.setdefault("CUDA_VISIBLE_DEVICES", "")
    os.environ.setdefault("OMP_NUM_THREADS", "1")
    os.environ.setdefault("MKL_NUM_THREADS", "1")
    os.environ.setdefault("OPENBLAS_NUM_THREADS", "1")
    os.environ.setdefault("NUMEXPR_NUM_THREADS", "1")


def run_case(case: dict) -> dict:
    start = time.perf_counter()
    result = solve(
        N=case["N"],
        chi=case["chi"],
        steps=case["steps"],
        lr=case["lr"],
        seed=case["seed"],
        device="cpu",
    )
    elapsed = time.perf_counter() - start
    N = result["N"]
    result["runtime_s"] = elapsed
    result["energy_per_bond"] = result["final_E"] / max(1, N - 1)
    result["exact_energy_per_bond"] = result["exact_E0"] / max(1, N - 1)
    result["thermodynamic_reference"] = BETHE_E0
    result["pass"] = bool((not result.get("below_ground", False)) and result["rel_err"] < 0.2)
    return result


def render_markdown(results: list[dict]) -> str:
    lines = []
    lines.append("# Stage 2 Heisenberg Benchmark Report")
    lines.append("")
    lines.append("Generated by `scripts/run_heisenberg_benchmark.py`.")
    lines.append("")
    lines.append("## Convention")
    lines.append("")
    lines.append("`H = J * sum_i S_i . S_{i+1}`, with `S = sigma / 2`, `J = 1.0`, open boundary condition.")
    lines.append("")
    lines.append("## Benchmark table")
    lines.append("")
    lines.append("| N | chi | seed | steps | exact_E0 | final_E | energy per bond | abs_err | runtime_s | pass/fail |")
    lines.append("|---:|---:|---:|---:|---:|---:|---:|---:|---:|:---|")
    for r in results:
        lines.append(
            f"| {r['N']} | {r['chi']} | {r['seed']} | {r['steps']} | "
            f"{r['exact_E0']:.12g} | {r['final_E']:.12g} | {r['energy_per_bond']:.12g} | "
            f"{r['abs_err']:.3g} | {r['runtime_s']:.3f} | {'PASS' if r['pass'] else 'FAIL'} |"
        )
    lines.append("")
    lines.append("## Thermodynamic-limit trend reference")
    lines.append("")
    lines.append(f"For the infinite spin-1/2 antiferromagnetic Heisenberg chain, `1/4 - ln(2) ~= {BETHE_E0:.16f}`.")
    lines.append("")
    lines.append("This is a trend reference for large systems, not a strict target for small finite open chains.")
    lines.append("")
    lines.append("## Observable validation")
    lines.append("")
    lines.append("See `tests/test_observables_dense_compare.py` and `tests/test_entanglement_entropy.py`.")
    lines.append("")
    lines.append("## Known limitations")
    lines.append("")
    lines.append("- Stage 2 fast benchmark is intentionally small and CPU-only.")
    lines.append("- If MPS observables use dense conversion internally, they are correctness references rather than efficient large-N algorithms.")
    lines.append("- Full convergence curves should be run explicitly, not inside pytest.")
    lines.append("")
    return "\n".join(lines)


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--preset", choices=sorted(PRESETS), default="tiny")
    parser.add_argument("--json-output", type=Path, default=None)
    parser.add_argument("--markdown-output", type=Path, default=None)
    parser.add_argument("--print", action="store_true", help="Print JSON results to stdout.")
    args = parser.parse_args()

    _env_setup()
    results = [run_case(case) for case in PRESETS[args.preset]]

    if args.json_output:
        args.json_output.parent.mkdir(parents=True, exist_ok=True)
        args.json_output.write_text(json.dumps(results, indent=2), encoding="utf-8")

    if args.markdown_output:
        args.markdown_output.parent.mkdir(parents=True, exist_ok=True)
        args.markdown_output.write_text(render_markdown(results), encoding="utf-8")

    if args.print or not args.json_output:
        print(json.dumps(results, indent=2))

    failed = [r for r in results if not r["pass"]]
    return 1 if failed else 0


if __name__ == "__main__":
    raise SystemExit(main())
